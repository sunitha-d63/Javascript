// Equality Check
let x=5;
console.log(x=='5');
console.log(x==='5');

// Inequality Check
let a=10;
console.log(a!=15);

// Greater Than Comparison
let X=25;
let Y=20;
console.log(X>Y);

// Less Than Comparison
let A=8;
let B=12;
console.log(A<B);

// Greater Than or Equal Comparison
let b=30;
let c=30;
console.log(b>=c)

// Less Than or Equal Comparison
let d=7;
let e=10;
console.log(d<=e);

// AND Operator
console.log(10>5&&20>15);

// OR Operator
console.log(5>10 || 15>10);

// NOT Operator
console.log (!(10>20));

// Combined AND and OR
console.log ((10>5 && 15>20)||(5<10));

// Between Range Check
console.log(15>10 && 12<20);

// Outside Range Check--------
console.log(25>10 && 20<25);

// Equality with NOT
console.log(5!=="5");

// Multiple Comparisons
console.log(10>5&&20>15&&30<40);

// Opposite Conditions
console.log(10>20 && 5<15);
console.log(!(10>20 && 5<15));